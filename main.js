let nCount = selector => {
  $(selector).each(function () {
    $(this)
      .animate({
        Counter: $(this).text()
      }, {
        duration: 4000,
        easing: "swing",

        step: function (value) {
          $(this).text(Math.ceil(value));
        }
      });
  });
};

